let number = prompt("Введіть число від 1 до 12");
if(number <= 1 && number<=2){
    alert('Зима')
}
else if(number <= 3 && number<=5){
    alert('Весна')
}
else if(number <= 6 && number<=8){
    alert('Літо')
}
else if(number <= 9 && number<=11){
    alert('Осінь')
}
else if(number = 12){
    alert('Зима')
}
else{
    alert('Error')
}